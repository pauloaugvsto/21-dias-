//opçao
var escolha = prompt('Escolhe um número de 1 a 5')

//casos 
switch (escolha) {
    case "1":
        console.log("Você escolheu a opção 1")
        break;
    case "2":
        console.log("Você escolheu a opção 2")
        break;
    default:
        console.log("Opção inválida")
        break;
}
